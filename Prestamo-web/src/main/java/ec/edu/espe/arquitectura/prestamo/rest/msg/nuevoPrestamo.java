package ec.edu.espe.arquitectura.prestamo.rest.msg;

/**
 *
 * @author Steven
 */
public class nuevoPrestamo {
    private int cli_id;
    private String tipoPrestamo;
    private String fechaCreacion;
    private String fechaConsecion;
    private String fechaDesembolso;
    private double monto;
    private int plazo;
    private double montoFinal;

    public int getCli_id() {
        return cli_id;
    }

    public void setCli_id(int cli_id) {
        this.cli_id = cli_id;
    }

    public String getTipoPrestamo() {
        return tipoPrestamo;
    }

    public void setTipoPrestamo(String tipoPrestamo) {
        this.tipoPrestamo = tipoPrestamo;
    }

    public String getFechaCreacion() {
        return fechaCreacion;
    }

    public void setFechaCreacion(String fechaCreacion) {
        this.fechaCreacion = fechaCreacion;
    }

    public String getFechaConsecion() {
        return fechaConsecion;
    }

    public void setFechaConsecion(String fechaConsecion) {
        this.fechaConsecion = fechaConsecion;
    }

    public String getFechaDesembolso() {
        return fechaDesembolso;
    }

    public void setFechaDesembolso(String fechaDesembolso) {
        this.fechaDesembolso = fechaDesembolso;
    }

    public double getMonto() {
        return monto;
    }

    public void setMonto(double monto) {
        this.monto = monto;
    }

    public int getPlazo() {
        return plazo;
    }

    public void setPlazo(int plazo) {
        this.plazo = plazo;
    }

    public double getMontoFinal() {
        return montoFinal;
    }

    public void setMontoFinal(double montoFinal) {
        this.montoFinal = montoFinal;
    }
    
    
}
